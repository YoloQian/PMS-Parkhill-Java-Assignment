    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        // TODO add your handling code here:
        // Get form data
        String checkpoint = checkpointTF.getText();
        String DateTime = dateTimeLabel.getText();

        // Validate form data
        if (checkpoint.isEmpty() || DateTime.equals("No Time Selected")) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (checkpoint.contains(";")) {
            JOptionPane.showMessageDialog(this, "Please remove semicolons (;) from input", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Write data to file
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("src/main/java/assignment/assignment/TxtFile/PatrolSchedule.txt", true))) {
            writer.write(checkpoint + ";"  + DateTime );
            writer.newLine();
            JOptionPane.showMessageDialog(this, "Patrol Schedule updated successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            // Clear form fields after successful submission
            checkpointTF.setText("");
            dateTimeLabel.setText("No Time Selected");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error writing to file", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_submitBtnActionPerformed

