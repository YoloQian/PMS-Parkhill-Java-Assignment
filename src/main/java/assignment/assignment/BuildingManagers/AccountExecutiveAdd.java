    public static String generateNewUserID(String fileName) {
            String lastUserID = null;
            try (BufferedReader br = new BufferedReader(new FileReader(fileName))) {
                String line;
                br.readLine();
                while ((line = br.readLine()) != null) {
                    String[] parts = line.split(";");
                    lastUserID = parts[0];
                }
            } catch (IOException e) {
            }

            String NewUserID;
            if (lastUserID == null) {
                NewUserID = "1";
            } else {
                int num = Integer.parseInt(lastUserID) + 1;
                NewUserID = String.valueOf(num);
            }
            return NewUserID;
        }

    public static String generateNewAccountexeID(String fileNameaccountexe) {
            String lastAccountExecutiveID = null;
            try (BufferedReader braccountexe = new BufferedReader(new FileReader(fileNameaccountexe))) {
                String line;
                braccountexe.readLine();
                while ((line = braccountexe.readLine()) != null) {
                    lastAccountExecutiveID = line;
                }
            } catch (IOException e) {
            }

            String NewAccountExecutiveID;
            if (lastAccountExecutiveID == null) {
                NewAccountExecutiveID = "AccExe001";
            } else {
                int num = Integer.parseInt(lastAccountExecutiveID.substring(6,9));
                num++;
                NewAccountExecutiveID = "AccExe" + String.format("%03d", num);
            }
            return NewAccountExecutiveID;
        }

