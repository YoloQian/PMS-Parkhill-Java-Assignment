    public static String generateNewTeamStructureID(String fileName) {
        String lastTeamStructureID = null;
        try (BufferedReader br = new BufferedReader(new FileReader(fileName))) {
            String line;
            br.readLine();
            while ((line = br.readLine()) != null) {
                lastTeamStructureID = line;
            }
        } catch (IOException e) {
        }
        
        String NewTeamStructureID;
        if (lastTeamStructureID == null) {
            NewTeamStructureID = "TeamStruc001";
        } else {
            int num = Integer.parseInt(lastTeamStructureID.substring(9,12));
            num++;
            NewTeamStructureID = "TeamStruc" + String.format("%03d", num);
        }
        return NewTeamStructureID;
    }

