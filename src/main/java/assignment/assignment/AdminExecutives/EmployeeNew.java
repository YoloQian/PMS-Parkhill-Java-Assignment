    public static String generateNewUserID(String fileName) {
            String lastUserID = null;
            try (BufferedReader br = new BufferedReader(new FileReader(fileName))) {
                String line;
                br.readLine();
                while ((line = br.readLine()) != null) {
                    String[] parts = line.split(";");
                    lastUserID = parts[0];
                }
            } catch (IOException e) {
            }

            String NewUserID;
            if (lastUserID == null) {
                NewUserID = "1";
            } else {
                int num = Integer.parseInt(lastUserID) + 1;
                NewUserID = String.valueOf(num);
            }
            return NewUserID;
        }

    public static String generateNewEmployeeID(String fileNameemployee) {
            String lastEmployeeID = null;
            try (BufferedReader bremployee = new BufferedReader(new FileReader(fileNameemployee))) {
                String line;
                bremployee.readLine();
                while ((line = bremployee.readLine()) != null) {
                    lastEmployeeID = line;
                }
            } catch (IOException e) {
            }

            String NewEmployeeID;
            if (lastEmployeeID == null) {
                NewEmployeeID = "Employee001";
            } else {
                int num = Integer.parseInt(lastEmployeeID.substring(8,11));
                num++;
                NewEmployeeID = "Employee" + String.format("%03d", num);
            }
            return NewEmployeeID;
        }
    
