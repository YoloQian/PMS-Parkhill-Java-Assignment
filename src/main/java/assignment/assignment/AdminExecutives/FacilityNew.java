    public static String generateNewFacilityID(String fileName) {
        String lastFacilityID = null;
        try (BufferedReader br = new BufferedReader(new FileReader(fileName))) {
            String line;
            br.readLine();
            while ((line = br.readLine()) != null) {
                lastFacilityID = line;
            }
        } catch (IOException e) {
        }
        
        String NewFacilityID;
        if (lastFacilityID == null) {
            NewFacilityID = "Facility001";
        } else {
            int num = Integer.parseInt(lastFacilityID.substring(8,11));
            num++;
            NewFacilityID = "Facility" + String.format("%03d", num);
        }
        return NewFacilityID;
    }
    
