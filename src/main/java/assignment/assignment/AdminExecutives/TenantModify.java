    private void searchidBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchidBTNActionPerformed
        // TODO add your handling code here:
        String searchUserID = searchidTF.getText();
        DefaultTableModel model = (DefaultTableModel) tenantmodifyTABLE.getModel();
        int rowCount = model.getRowCount();
        boolean userFound = false;
        for (int i = 0; i < rowCount; i++) {
            if (model.getValueAt(i, 0).equals(searchUserID)) {
                tenantmodifyTABLE.setRowSelectionInterval(i, i);
                userFound = true;
                break;
            }
        }
        if (!userFound) {
            JOptionPane.showMessageDialog(this, "User with ID " + searchUserID + " not found!");
        }
    }//GEN-LAST:event_searchidBTNActionPerformed

    private void viewtenantBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewtenantBTNActionPerformed
        // TODO add your handling code here:
        try {
            File userInfoFile = new File("src/main/java/assignment/assignment/TxtFile/UserInfo.txt");
            File tenantInfoFile = new File("src/main/java/assignment/assignment/TxtFile/TenantInfo.txt");
            FileReader userInfoReader = new FileReader(userInfoFile);
            FileReader tenantInfoReader = new FileReader(tenantInfoFile);
            BufferedReader userInfoBR = new BufferedReader(userInfoReader);
            BufferedReader tenantInfoBR = new BufferedReader(tenantInfoReader);
            String userInfoLine, tenantInfoLine;
            DefaultTableModel model = (DefaultTableModel) tenantmodifyTABLE.getModel();
            model.setRowCount(0);
            userInfoBR.readLine(); // Skip header row in UserInfo.txt
            tenantInfoBR.readLine(); // Skip header row in TenantInfo.txt
            while ((userInfoLine = userInfoBR.readLine()) != null) {
                String[] userInfoData = userInfoLine.split(";");
                while ((tenantInfoLine = tenantInfoBR.readLine()) != null) {
                    String[] tenantInfoData = tenantInfoLine.split(";");
                    if (tenantInfoData[1].equals(userInfoData[0])) { // Match UserID
                        Object[] row = { tenantInfoData[0], userInfoData[0], userInfoData[3], userInfoData[4], userInfoData[1] };
                        model.addRow(row);
                        break; // Exit tenantInfo loop when match is found
                    }
                }
                tenantInfoBR.close();
                tenantInfoReader = new FileReader(tenantInfoFile); // Reset tenantInfoReader
                tenantInfoBR = new BufferedReader(tenantInfoReader); // Reset tenantInfoBR
                tenantInfoBR.readLine(); // Skip header row in TenantInfo.txt
            }
            userInfoBR.close();
            userInfoReader.close();
            tenantInfoBR.close();
            tenantInfoReader.close();
        } catch (IOException e) {
            System.out.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_viewtenantBTNActionPerformed

