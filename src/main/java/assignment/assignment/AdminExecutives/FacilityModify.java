    private void viewfacilityBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewfacilityBTNActionPerformed
        // TODO add your handling code here:
        try {
            File file = new File("src/main/java/assignment/assignment/TxtFile/Facility.txt");
            FileReader fr = new FileReader(file);
            BufferedReader br = new BufferedReader(fr);
            String line;
            DefaultTableModel model = (DefaultTableModel) facilitymodifyTABLE.getModel();
            model.setRowCount(0);
            br.readLine();
            while ((line = br.readLine()) != null) {
                String[] data = line.split(";");
                    Object[] row = { data[0], data[1],data[2], data[3]};
                    model.addRow(row);
                
            }
            br.close();
            fr.close();
        } catch (IOException e) {
            System.out.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_viewfacilityBTNActionPerformed

    private void deleteBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBTNActionPerformed
        // TODO add your handling code here:
        int selectedRow = facilitymodifyTABLE.getSelectedRow();
        if (selectedRow != -1) {
            DefaultTableModel model = (DefaultTableModel) facilitymodifyTABLE.getModel();
            model.removeRow(selectedRow);
            // save the updated data back to the file
            try (FileWriter fw = new FileWriter("Facility.txt");
                 BufferedWriter bw = new BufferedWriter(fw);
                 PrintWriter pw = new PrintWriter(bw)) {
                for (int i = 0; i < model.getRowCount(); i++) {
                    for (int j = 0; j < model.getColumnCount(); j++) {
                        pw.print(model.getValueAt(i, j));
                        if (j != model.getColumnCount() - 1) {
                            pw.print(";");
                        }
                    }
                    pw.println();
                }
            } catch (IOException e) {
            }
        }
    }//GEN-LAST:event_deleteBTNActionPerformed

